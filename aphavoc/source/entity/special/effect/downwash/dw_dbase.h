typedef enum DOWNWASH_TYPES
{
	DOWNWASH_TYPE_NONE,

	DOWNWASH_TYPE_LAND,
	
	DOWNWASH_TYPE_LAND_DUAL_ROTORS,

	DOWNWASH_TYPE_WATER,
	
	DOWNWASH_TYPE_WATER_DUAL_ROTORS,

	NUM_DOWNWASH_TYPES
} downwash_types;

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct DOWNWASH_COMPONENT
{
	int
		trail_count;

	unsigned char
		create_in_all_quadrants : 1;

	float
		generator_lifetime,
		frequency,
		lifetime_min,
		lifetime_max,
		scale_min,
		scale_max;
};

typedef struct DOWNWASH_COMPONENT downwash_component;

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

struct DOWNWASH_DATA
{
	int
		number_of_components;

	downwash_component
		*component;
};

typedef struct DOWNWASH_DATA downwash_data;

